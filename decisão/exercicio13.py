print("Número correspondente a dia da semana")
print()

numero_dia = int(input("Insira um número: "))
if numero_dia == 1:
    print(" O numero 1 corresponde a Domingo")
elif numero_dia ==2:
    print(" O numero 2 corresponde a Segunda")
elif numero_dia ==3:
    print(" O numero 3 corresponde a Terça")
elif numero_dia ==4:
    print(" O numero 4 corresponde a Quarta")
elif numero_dia ==5:
    print(" O numero 5 corresponde a Quinta")
elif numero_dia ==6:
    print(" O numero 6 corresponde a Sexta")
elif numero_dia ==7:
    print(" O numero 7 corresponde a Sabádo")

else:
    print("Valor Inválido")











