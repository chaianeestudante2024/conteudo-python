# Conversão de gigabye para terabyte:

gigabyte = int(input("Quantos gigabytes deseja converter para terabytes?: "))

# Cálculo de Conversão:
terabyte = gigabyte / 1024

# Aqui mostrará o resultado dessa conversão de gigabyte para terabyte:
print("O valor dessa conversão é de",terabyte,"terabytes")

